frase = str(input("Digite uma frase: "))

espaço = frase.count(' ')
a = frase.count('a')
e = frase.count('e')
i = frase.count('i')
o = frase.count('o')
u = frase.count('u')

print("\nHá", espaço, "Espaço(s) na sua frase","\nHá", a, "letras(s) 'a' na sua frase","\nHá", e, "letras(s) 'e' na sua frase","\nHá", i, "letras(s) 'i' na sua frase","\nHá", o, "letras(s) 'o' na sua frase","\nHá", u, "letras(s) 'u' na sua frase")
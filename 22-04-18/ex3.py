final_placa = int(input("Digite o final da sua placa: "))

if (final_placa == 1) or (final_placa == 2):
	print ("Não circula de segunda-feira")
elif (final_placa == 3) or (final_placa == 4):
	print ("Não circula de terça-feira")
elif (final_placa == 5) or (final_placa == 6):
	print ("Não circula de quarta-feira")
elif (final_placa == 7) or (final_placa == 8):
	print ("Não circula de quinta-feira")
elif (final_placa == 9) or (final_placa == 0):
	print ("Não circula de sexta-feira")